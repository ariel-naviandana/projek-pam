package com.example.projekPam;

public class DaftarTemanEntry {
    private String nama;
    private String username;

    public DaftarTemanEntry(String nama, String username) {
        this.nama = nama;
        this.username = username;
    }

    public String getNama() {
        return nama;
    }

    public String getUsername() {
        return username;
    }

}